function multiplica() {
    num1 = document.getElementById("n1").value;
    num2 = document.getElementById("n2").value;
    document.getElementById("resultado").innerHTML = num1 * num2;
}

function divide() {
    num1 = document.getElementById("n1").value;
    num2 = document.getElementById("n2").value;
    document.getElementById("resultado").innerHTML = num1 / num2;
}